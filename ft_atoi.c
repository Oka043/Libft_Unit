int	ft_atoi(const char *s)
{
	int				f;
	long long int	res;

	res = 0;
	while (*s == ' ' || *s == '\t' || *s == '\n'
			|| *s == '\v' || *s == '\f' || *s == '\r')
		s++;
	if (*s == '+' || *s == '-')
	{
		if (*s == '-')
			f = -1;
		s++;
		if (!(*s <= '9' && *s >= '0') && *s != '-')
			return (0);
	}
	while (*s <= '9' && *s >= '0')
	{
		res = res * 10 + *s - 48;
		s++;
		if (res > 2147483648 && f == 1)
			return (-1);
		else if (res > 2147483648 && f == -1)
			return (0);
	}
	return (f == -1 ? -res : res);
}
